package com.github.hakandindis.plugins.adbhub.ui.theme.spacing

import androidx.compose.ui.unit.dp

/**
 * ADB Hub Spacing definitions
 * Standardized padding, margin, and gap values
 */
object AdbHubSpacing {
    // Padding
    object Padding {
        val XS = 4.dp
        val SM = 8.dp
        val MD = 12.dp
        val LG = 16.dp
        val XL = 24.dp
        val XXL = 32.dp
    }

    // Margin
    object Margin {
        val XS = 4.dp
        val SM = 8.dp
        val MD = 12.dp
        val LG = 16.dp
        val XL = 24.dp
    }

    // Gap (for Arrangement.spacedBy)
    object Gap {
        val XS = 4.dp
        val SM = 8.dp
        val MD = 12.dp
        val LG = 16.dp
        val XL = 20.dp
    }
}
